package ru.captainxsander;

/**
 * Режимы игры автомата.
 * Они определяют, какие игрушки появляются в куче
 * и должен ли выигрыш открывать карточки зверинца.
 */
public enum GameMode {
    /**
     * Обычный режим без открытия карточек зверинца.
     */
    NORMAL,

    /**
     * Режим спасения зверей.
     * В нём первая победа по уникальной игрушке
     * открывает её карточку в зверинце.
     */
    RESCUE,

    /**
     * Режим поиска зверей.
     * В нём в куче доступны все игрушки, а клешней можно
     * управлять даже после захвата — до самого сброса в лоток.
     * Такой режим нужен, чтобы вручную "разгребать" кучу
     * и находить конкретную игрушку.
     */
    FIND_ANIMAL,

    /**
     * Режим "Пойма кота".
     * Игроку нужно поймать кота с нужной эмоцией из движущейся кучи
     * и доставить именно его в лоток.
     */
    CATCH_CAT
}
